list1 = [2, 5, 3, 4, 1]
list2 = ['Jennie', 'Lisa', 'Rose', 'Jisoo']
list3 = list2.copy()

#To sort everything in the list in accesending / alphabetical order {use SORT}
list2.sort()
print(list2)

list1.sort()
print(list1)

#To sort everything in the list in reverse order {use REVERSE}
list2.reverse()
print(list2)

list1.reverse()
print(list1)

#To copy everything in the list for another list in reverse order {use REVERSE}
list3 = list2.copy()
print(list3)

#To remove the last value in the list
list1.pop()
print(list1)

list2.pop()
print(list2)
